import { 是, type 坐标, type 资源标识 } from '../运行时/全局常量.ts';
import { 接口 } from "../运行时/易次元.ts";

interface 按钮 {
    资源标识: 资源标识;
    位置: 坐标;
}

interface 构造参数 {
    资源标识: 资源标识;
    确认按钮: 按钮;
    取消按钮: 按钮;
}

export class 简单确认弹框类 {
    #资源标识: 资源标识;
    #确认按钮: 按钮;
    #取消按钮: 按钮;

    constructor(参数: 构造参数) {
        this.#资源标识 = 参数.资源标识;
        this.#确认按钮 = 参数.确认按钮;
        this.#取消按钮 = 参数.取消按钮;
    }

    async 显示() {
        await 接口.创建图层("确认弹框_图层", {
            位置: { 横: 640, 纵: 720 },
            锚点: { 横: 50, 纵: 50 },
            裁剪模式: 是,
            裁剪区域: { 宽: 1280, 高: 720 },
            事件阻挡: 是,
        });
    }
}
